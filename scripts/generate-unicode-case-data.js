#!/usr/bin/env node

const fs = require("fs");
const path = require("path");
const { downloadText } = require("./lib/resource-container");

const REPO_ROOT = path.resolve(__dirname, "..");
const DEFAULT_UNICODE_VERSION = "17.0.0";
const DEFAULT_OUTPUT = path.join(
  REPO_ROOT,
  "source",
  "generated",
  "Generated.UnicodeCaseData.pas",
);

function parseArguments() {
  if (process.argv.length > 4) {
    throw new Error(
      "Usage: node scripts/generate-unicode-case-data.js " +
      "[unicode-version] [output-file]",
    );
  }
  return {
    unicodeVersion: process.argv[2] || DEFAULT_UNICODE_VERSION,
    outputFile: process.argv[3]
      ? path.resolve(process.argv[3])
      : DEFAULT_OUTPUT,
  };
}

async function downloadUCDFile(version, name) {
  const url = `https://www.unicode.org/Public/${version}/ucd/${name}`;
  console.log(`Downloading ${name}...`);
  return downloadText(url);
}

function codePointSequence(value) {
  if (!value.trim()) {
    return [];
  }
  return value.trim().split(/\s+/).map((item) => parseInt(item, 16));
}

function parseUnicodeData(text) {
  const lower = new Map();
  const upper = new Map();
  for (const line of text.split(/\r?\n/)) {
    if (!line) {
      continue;
    }
    const fields = line.split(";");
    const source = parseInt(fields[0], 16);
    if (fields[12]) {
      upper.set(source, [parseInt(fields[12], 16)]);
    }
    if (fields[13]) {
      lower.set(source, [parseInt(fields[13], 16)]);
    }
  }
  return { lower, upper };
}

function applySpecialCasing(text, mappings) {
  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.replace(/#.*/, "").trim();
    if (!line) {
      continue;
    }
    const fields = line.split(";").map((field) => field.trim());
    const source = parseInt(fields[0], 16);
    const condition = fields[4];
    if (!condition) {
      mappings.lower.set(source, codePointSequence(fields[1]));
      mappings.upper.set(source, codePointSequence(fields[3]));
    } else if (condition === "Final_Sigma") {
      // Emitted algorithmically by TextSemantics. Locale-specific entries
      // do not participate in Unicode Default Case Conversion.
      if (source !== 0x03a3 || fields[1] !== "03C2") {
        throw new Error("Unexpected Final_Sigma special-casing entry");
      }
    }
  }
}

function parsePropertyRanges(text, propertyName) {
  const ranges = [];
  for (const rawLine of text.split(/\r?\n/)) {
    const line = rawLine.replace(/#.*/, "").trim();
    if (!line) {
      continue;
    }
    const [rangeText, property] = line.split(";").map((part) => part.trim());
    if (property !== propertyName) {
      continue;
    }
    const bounds = rangeText.split("..").map((item) => parseInt(item, 16));
    ranges.push([bounds[0], bounds.length === 1 ? bounds[0] : bounds[1]]);
  }
  return ranges;
}

function utf16CodeUnits(codePoint) {
  if (codePoint <= 0xffff) {
    return [codePoint];
  }
  const supplementary = codePoint - 0x10000;
  return [
    0xd800 + (supplementary >> 10),
    0xdc00 + (supplementary & 0x3ff),
  ];
}

function flattenMappings(mapping) {
  const sources = [];
  const offsets = [];
  const lengths = [];
  const codeUnits = [];
  for (const [source, sequence] of [...mapping].sort((a, b) => a[0] - b[0])) {
    sources.push(source);
    offsets.push(codeUnits.length);
    const units = sequence.flatMap(utf16CodeUnits);
    lengths.push(units.length);
    codeUnits.push(...units);
  }
  return { sources, offsets, lengths, codeUnits };
}

function pascalNumber(value) {
  return `$${value.toString(16).toUpperCase()}`;
}

function emitArray(lines, name, type, values, perLine = 12) {
  lines.push(`  ${name}: array[0..${values.length - 1}] of ${type} = (`);
  for (let start = 0; start < values.length; start += perLine) {
    const row = values.slice(start, start + perLine).map(pascalNumber);
    const suffix = start + perLine < values.length ? "," : "";
    lines.push(`    ${row.join(", ")}${suffix}`);
  }
  lines.push("  );");
}

function generatePascal(version, lower, upper, cased, caseIgnorable) {
  const lines = [
    "unit Generated.UnicodeCaseData;",
    "",
    "{$I Shared.inc}",
    "",
    "// Generated by scripts/generate-unicode-case-data.js",
    `// Source: Unicode ${version} UnicodeData.txt, SpecialCasing.txt,`,
    "// and DerivedCoreProperties.txt.",
    "",
    "interface",
    "",
    `const GeneratedUnicodeCaseDataVersion = '${version}';`,
    "",
    "function TryGetUnicodeDefaultLowercase(const ACodePoint: Cardinal;",
    "  out AMapping: string): Boolean;",
    "function TryGetUnicodeDefaultUppercase(const ACodePoint: Cardinal;",
    "  out AMapping: string): Boolean;",
    "function IsUnicodeCased(const ACodePoint: Cardinal): Boolean;",
    "function IsUnicodeCaseIgnorable(const ACodePoint: Cardinal): Boolean;",
    "",
    "implementation",
    "",
    "const",
  ];

  const lowerData = flattenMappings(lower);
  const upperData = flattenMappings(upper);
  emitArray(lines, "LOWER_SOURCES", "Cardinal", lowerData.sources);
  emitArray(lines, "LOWER_OFFSETS", "Cardinal", lowerData.offsets);
  emitArray(lines, "LOWER_LENGTHS", "Byte", lowerData.lengths);
  emitArray(lines, "LOWER_CODE_UNITS", "Word", lowerData.codeUnits);
  emitArray(lines, "UPPER_SOURCES", "Cardinal", upperData.sources);
  emitArray(lines, "UPPER_OFFSETS", "Cardinal", upperData.offsets);
  emitArray(lines, "UPPER_LENGTHS", "Byte", upperData.lengths);
  emitArray(lines, "UPPER_CODE_UNITS", "Word", upperData.codeUnits);
  emitArray(lines, "CASED_RANGES", "Cardinal", cased.flat());
  emitArray(lines, "CASE_IGNORABLE_RANGES", "Cardinal", caseIgnorable.flat());

  lines.push(`
function FindSource(const ACodePoint: Cardinal; const ASources: array of Cardinal): Integer;
var
  LowIndex, HighIndex, MiddleIndex: Integer;
begin
  LowIndex := 0;
  HighIndex := Length(ASources) - 1;
  while LowIndex <= HighIndex do
  begin
    MiddleIndex := LowIndex + (HighIndex - LowIndex) div 2;
    if ASources[MiddleIndex] = ACodePoint then
      Exit(MiddleIndex);
    if ASources[MiddleIndex] < ACodePoint then
      LowIndex := MiddleIndex + 1
    else
      HighIndex := MiddleIndex - 1;
  end;
  Result := -1;
end;

function BuildMapping(const AIndex: Integer; const AOffsets: array of Cardinal;
  const ALengths: array of Byte; const ACodeUnits: array of Word): string;
var
  I: Integer;
begin
  SetLength(Result, ALengths[AIndex]);
  for I := 0 to ALengths[AIndex] - 1 do
    Result[I + 1] := Char(ACodeUnits[AOffsets[AIndex] + Cardinal(I)]);
end;

function TryGetUnicodeDefaultLowercase(const ACodePoint: Cardinal;
  out AMapping: string): Boolean;
var
  Index: Integer;
begin
  Index := FindSource(ACodePoint, LOWER_SOURCES);
  Result := Index >= 0;
  if Result then
    AMapping := BuildMapping(Index, LOWER_OFFSETS, LOWER_LENGTHS,
      LOWER_CODE_UNITS)
  else
    AMapping := '';
end;

function TryGetUnicodeDefaultUppercase(const ACodePoint: Cardinal;
  out AMapping: string): Boolean;
var
  Index: Integer;
begin
  Index := FindSource(ACodePoint, UPPER_SOURCES);
  Result := Index >= 0;
  if Result then
    AMapping := BuildMapping(Index, UPPER_OFFSETS, UPPER_LENGTHS,
      UPPER_CODE_UNITS)
  else
    AMapping := '';
end;

function IsInRanges(const ACodePoint: Cardinal;
  const ARanges: array of Cardinal): Boolean;
var
  LowIndex, HighIndex, MiddleIndex: Integer;
begin
  LowIndex := 0;
  HighIndex := Length(ARanges) div 2 - 1;
  while LowIndex <= HighIndex do
  begin
    MiddleIndex := LowIndex + (HighIndex - LowIndex) div 2;
    if ACodePoint < ARanges[MiddleIndex * 2] then
      HighIndex := MiddleIndex - 1
    else if ACodePoint > ARanges[MiddleIndex * 2 + 1] then
      LowIndex := MiddleIndex + 1
    else
      Exit(True);
  end;
  Result := False;
end;

function IsUnicodeCased(const ACodePoint: Cardinal): Boolean;
begin
  Result := IsInRanges(ACodePoint, CASED_RANGES);
end;

function IsUnicodeCaseIgnorable(const ACodePoint: Cardinal): Boolean;
begin
  Result := IsInRanges(ACodePoint, CASE_IGNORABLE_RANGES);
end;

end.`);
  return `${lines.join("\n")}\n`;
}

async function main() {
  const { unicodeVersion, outputFile } = parseArguments();
  const [unicodeData, specialCasing, derivedCore] = await Promise.all([
    downloadUCDFile(unicodeVersion, "UnicodeData.txt"),
    downloadUCDFile(unicodeVersion, "SpecialCasing.txt"),
    downloadUCDFile(unicodeVersion, "DerivedCoreProperties.txt"),
  ]);
  const mappings = parseUnicodeData(unicodeData);
  applySpecialCasing(specialCasing, mappings);
  const cased = parsePropertyRanges(derivedCore, "Cased");
  const caseIgnorable = parsePropertyRanges(derivedCore, "Case_Ignorable");
  fs.writeFileSync(
    outputFile,
    generatePascal(
      unicodeVersion,
      mappings.lower,
      mappings.upper,
      cased,
      caseIgnorable,
    ),
    "utf8",
  );
  console.log(`Wrote ${outputFile}`);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
